package com.matsak.exhibitionhall.db.entity;

public class Theme {
    private int id;
    private String theme_name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getThemeName() {
        return theme_name;
    }

    public void setThemeName(String theme_name) {
        this.theme_name = theme_name;
    }
}
